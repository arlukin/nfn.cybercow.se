<br><br><br><br>
starting <br>
It's probably easiest to start out with a few swings. Although various chokes and locks can be applied with nunchaku by far the most fun part of practicing is attempting to produce an impenetrable mass of whirring wood around your body! There are several obstacles to this goal. First you must be able to catch or recover the nunchaku after swinging. Secondly you should be able to flow from one move to the next. Thirdly, and most importantly, it's a very good idea not to do any swings that are going to end up going through your own head! This point seems obvious but it is very easy to hit your own head by just flailing round with the nunchaku (and I've done it many times myself: when I began using nunchaku the back of my head seemed to almost magnetically attract them!)
<br><br>
Getting started with nunchaku<br><br>

Selecting a school<br><br>

It's a lot easier to learn how to use nunchaku with proper instruction than to try and do it on your own. Since nunchaku are generally considered Okinawan in origin they are most strongly associated with Karate. So your first stop is to look at local Karate schools. However not all Karate schools teach weapons and many will only do so to advanced students. If you are unable to find proper instruction then it is possible to learn to use nunchaku on your own. This page is designed for those who want to gain a basic understanding of nunchaku without formal instruction. It can never replace a proper teacher, and is not intended to.
<br><br>
Selecting your nunchaku<br><br>
<br><br>
Nunchaku are easily bought by mail order. I'm British and there is no problem with doing that here. However, I know that nunchaku are illegal in some American states so I suggest you check the law where you live.
But what do you actually want to order from the vast selection usually available? I suggest that you buy two nunchaku to start with. A pair of foam covered "safety chucks" and a pair of standard wooden nunchaku connected by a cord. Why buy these? First the safety nunchaku. Well, although some traditionalists might believe people learn faster with by starting with a proper pair (and this is probably true!) you will get a lot of bruises that way, especially if you are trying to teach yourself. Foam covered nunchaku allow you to play around with new moves at much reduced risk to yourself. Secondly the cord connected wooden nunchaku. Although many nunchaku on sale are connected by a chain these are not so suitable to learn with. This is because you can't do some moves with them (at least without pain) such as letting go of the nunchaku handles and allowing them to roll across your hand. You are also unable to adjust the length of the connection if it is made of chain.
<br><br>
Preparing your nunchaku<br><br>
<br><br>

<br>Once you've got the nunchaku you need to check that the cord is the correct length. The ideal length is the width of the hand, and the nunchaku should be adjusted to this length. You will have also noticed that the safety nunchaku are lighter than the wooden ones. If the nunchaku are hollow then it is a good idea to try and add weight to them by sticking things securely to the inside. I'll leave the choice of what to use to your imagination but whatever you use make very sure that it isn't suddenly going to become detached and fly out of the end! This added weight will give the nunchaku better flight.
<br>
<br>
<br>So you want to learn advanced nunchaku?
<br>
<br>Advanced nunchaku techniques take time and effort to learn. They can be grouped into several categories:
<br>
<br>Locks and chokes
<br>Twirls
<br>Longer sequences of flowing strikes
<br>More advanced gathers
<br>Knowing the strengths and weaknesses of the nunchaku
<br>
<br>Ultimately I intend to consider each of these categories separately. You'll have to wait until I have time to expand this into a proper section but for the time being here are some things to think about:
<br>
<br>It's all very well swinging nunchaku around your body at high speed but due to their very nature (two sticks connected by a cord) they also make ideal choking instruments. One advanced nunchaku skill is to be able to enchance basic locks and choking techniques using nunchaku. This is an area that is often well covered in discussions of police nunchaku as such techniques can be used in a more measured fashion than nunchaku strikes.
<br>
<br>As well as simply passing the nunchaku from hand to hand another advanced nunchaku skill is to be able to change your grip on the nunchaku using just one hand. This is mediated by "twirling" the nunchaku in your hand - i.e. by letting go of one stick, allowing the nunchaku to pass across the back of your hand and then catching the other stick. I'll explain how to do this in the future. It's a technique that requires a good amount of practice before it can be done reliably. Don't try this one near anything breakable! I can personally guarantee that the nunchaku will fly out of your hand and destroy the most valuable item in the room!!!
<br>
<br>A very simple extension of basic nunchaku technique to advanced nunchaku technique is to develop the individual moves described in the basic section into flowing combinations of strikes. The kind of combinations that you want to try are sequences of moves found in nunchaku kata. In general the principle is to allow for multiple chances to hit your target in a sequence of flowing movements.
<br>
<br>Advanced gathers are an intrinsic part of advanced nunchaku technique. As you may have observed, when you hit something with nunchaku the free stick has a tendency to fly off out of control. You don't want to just keep swinging if that happens because the swings will be out of control and could well end up trying to go through your own head. Ouch!!! Instead you need to gather the nunchaku back up into a controlled position. It is possible to gather using your feet, legs, shoulders and even neck in addition to the basic body gather described in the basic technique section.
<br>
<br>To become really skilled with the nunchaku you need to understand their strengths and weaknesses. Nunchaku strengths include incredible speed and a great deal of flexibility in what can be done. Nunchaku weaknesses include difficulty in blocking (compare with Tonfa for instance) and the need to recover after striking. If you fully understand the weaknesses of nunchaku then you will be in a better position to overcome them.
<br>
<br>Hope this is enough food for thought on advanced nunchaku for a while!
<br>
<br>Whenever you have a free hand, DO SOMETHING WITH IT. Either hold it open palm in front of your chest (this is what I do) or cock it back at your hip. Don't just let it flop around or drop.
<br>STANCE. The deeper the better. It's good for your legs, it looks good, and it makes you pay attention to your whole
<br>body.
<br>GO SLOW. Don't race when you're first learning this or you'll crack yourself good. Remember, it's just practice..
<br>IT'S NOT JUST PRACTICE.. >=) Imagine each opponent and what he might be doing as you go through the routine.
<br>
